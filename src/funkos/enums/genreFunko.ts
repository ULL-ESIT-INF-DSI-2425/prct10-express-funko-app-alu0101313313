/**
 * Enum representing the genre of a Funko Pop.
 * 
 * @enum {string}
 * @property {string} MOVIE - Represents a Funko Pop from a movie.
 * @property {string} SERIES - Represents a Funko Pop from a series.
 * @property {string} VIDEOGAME - Represents a Funko Pop from a video game.
 * @property {string} ANIMATION - Represents a Funko Pop from an animation.
 * @property {string} SPORT - Represents a Funko Pop from a sport.
 * @property {string} MUSIC - Represents a Funko Pop from music.
 * @property {string} OTHER - Represents a Funko Pop from other genres.
 */
export enum GenreFunko {
  MOVIE = "MOVIE",
  SERIES = "SERIES",
  VIDEOGAME = "VIDEOGAME",
  ANIMATION = "ANIMATION",
  SPORT = "SPORT",
  MUSIC = "MUSIC",
  OTHER = "OTHER",
}
